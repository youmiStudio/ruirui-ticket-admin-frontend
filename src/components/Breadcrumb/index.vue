<template>
  <el-breadcrumb class="app-breadcrumb" separator="/">
    <transition-group name="breadcrumb">
      <el-breadcrumb-item v-for="(item, index) in levelList" :key="item.path">
        <span
          v-if="item.redirect === 'noRedirect' || index == levelList.length - 1"
          class="no-redirect"
          >{{ item.meta && item.meta.title }}</span
        >
        <a v-else @click.prevent="handleLink(item)">{{
          item.meta && item.meta.title
        }}</a>
      </el-breadcrumb-item>
    </transition-group>
  </el-breadcrumb>
</template>

<script lang="ts" setup name="Breadcrumb">
import  {compile} from 'path-to-regexp';
import { RouteLocationRaw } from 'vue-router';
import { BreadcrumbMatched } from './types';
import { Ref } from 'vue';

const route = useRoute();
const router = useRouter();

const levelList: Ref<BreadcrumbMatched[]> = ref([]);

watch(
  route,
  () => {
    if (route.path.startsWith('/redirect/')) {
      return;
    }
    getBreadcrumb()
  },
  { immediate: true }
);

onBeforeMount(()=>{
  getBreadcrumb()
})

function getBreadcrumb() {
  // only show routes with meta.title
  let matched: BreadcrumbMatched[] = route.matched.filter(
    (item) => item.meta && item.meta.title
  );
  const first = matched[0];

  if (!isDashboard(first)) {
    matched = [{ path: '/dashboard', meta: { title: 'Dashboard' } }].concat(
      matched
    );
  }

  levelList.value = matched.filter(
    (item) => item.meta && item.meta.title && item.meta.breadcrumb !== false
  );
}

function isDashboard(route: BreadcrumbMatched) {
  const name = route && route.name;
  if (!name) {
    return false;
  }
  return name.trim().toLocaleLowerCase() === 'Dashboard'.toLocaleLowerCase();
}

function pathCompile(path: string) {
  // To solve this problem https://github.com/PanJiaChen/vue-element-admin/issues/561
  const { params } = route;
  var toPath = compile(path);
  return toPath(params);
}

function handleLink(item: BreadcrumbMatched) {
  const { redirect, path } = item;
  if (redirect) {
    router.push(redirect as RouteLocationRaw);
    return;
  }
  router.push(pathCompile(path));
}
</script>

